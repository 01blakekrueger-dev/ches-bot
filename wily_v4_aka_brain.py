PIECE_VALUES = {chess.PAWN: 100, chess.KNIGHT: 320, chess.BISHOP: 330, chess.ROOK: 500, chess.QUEEN: 900, chess.KING: 20000}

class WillyV6:
    def __init__(self):
        self.q_table = {}
        self.stats = {"games": 0, "wins": 0}
        self.history = []
        self.load_brain()

    def load_brain(self):
        if os.path.exists(DB_PATH):
            try:
                with open(DB_PATH, 'rb') as f:
                    data = pickle.load(f)
                    self.q_table = data.get('q_table', {})
                    self.stats = data.get('stats', {"games": 0, "wins": 0})
            except: pass

    def save_brain(self):
        with open(DB_PATH, 'wb') as f:
            pickle.dump({'q_table': self.q_table, 'stats': self.stats}, f)
        with open(JSON_EXPORT_PATH, 'w') as f:
            json.dump(self.q_table, f)

    def evaluate_board(self, board):
        """V6: Strategic evaluation beyond just counting pieces."""
        if board.is_checkmate():
            return -99999 if board.turn == chess.WHITE else 99999

        score = 0
        for sq in chess.SQUARES:
            p = board.piece_at(sq)
            if p:
                val = PIECE_VALUES.get(p.piece_type, 0)
                if p.color == chess.BLACK: # Willy is Black
                    score += val
                    # Bonus for controlling the center
                    if sq in [chess.D4, chess.D5, chess.E4, chess.E5]: score += 10
                else:
                    score -= val
        return score

    def minimax(self, board, depth, alpha, beta, maximizing_player):
        """V6: Predictive Simulation (Alpha-Beta Pruning)"""
        if depth == 0 or board.is_game_over():
            return self.evaluate_board(board)

        if maximizing_player:
            max_eval = -math.inf
            for move in board.legal_moves:
                board.push(move)
                eval = self.minimax(board, depth - 1, alpha, beta, False)
                board.pop()
                max_eval = max(max_eval, eval)
                alpha = max(alpha, eval)
                if beta <= alpha: break
            return max_eval
        else:
            min_eval = math.inf
            for move in board.legal_moves:
                board.push(move)
                eval = self.minimax(board, depth - 1, alpha, beta, True)
                board.pop()
                min_eval = min(min_eval, eval)
                beta = min(beta, eval)
                if beta <= alpha: break
            return min_eval

    def get_move(self, board, depth=3):
        """Combines Memory with Prediction."""
        state = board.fen().split(' ')[0]

        # 1. Check for Memory-based Instant Win
        if state in self.q_table:
            best_memory_move = max(self.q_table[state], key=self.q_table[state].get)
            if self.q_table[state][best_memory_move] > 50: # Only if it's a high-confidence win
                return chess.Move.from_uci(best_memory_move)

        # 2. V6 Calculation: Look-ahead
        best_move = None
        max_eval = -math.inf

        for move in board.legal_moves:
            board.push(move)
            eval = self.minimax(board, depth - 1, -math.inf, math.inf, False)
            board.pop()
            if eval > max_eval:
                max_eval = eval
                best_move = move

        return best_move if best_move else random.choice(list(board.legal_moves))

bot = WillyV6()
