# --- 2. BACKEND HANDLERS ---
def handle_move(move_uci):
    global board
    if move_uci == "RESET": board.reset()
    else:
        try:
            move = chess.Move.from_uci(move_uci)
            if move in board.legal_moves:
                board.push(move)
                if not board.is_game_over():
                    m = bot.get_move(board, depth=3) # High-level play
                    if m: board.push(m)
                if board.is_game_over():
                    if board.result() == "0-1":
                        bot.stats["wins"] += 1
                        svg_data = chess.svg.board(board, size=400)
                        with open(f"{WINS_DIR}v6_win_{bot.stats['games']}.svg", "w") as f: f.write(svg_data)
                    bot.save_brain()
        except: pass
    return f"{board.fen()}|0|{len(bot.q_table)}|{bot.stats['wins']}"

def run_sims(count=1000):
    global board
    engine = chess.engine.SimpleEngine.popen_uci("/usr/games/stockfish")
    engine.configure({"Skill Level": 0})

    for g in range(int(count)):
        board.reset()
        while not board.is_game_over() and board.fullmove_number < 60:
            if board.turn == chess.WHITE:
                res = engine.play(board, chess.engine.Limit(time=0.00001))
                board.push(res.move)
            else:
                m = bot.get_move(board, depth=2) # Depth 2 for fast training
                board.push(m)

        if board.result() == "0-1":
            bot.stats["wins"] += 1
            svg_data = chess.svg.board(board, size=400)
            with open(f"{WINS_DIR}v6_sim_win_{bot.stats['games']}.svg", "w") as f: f.write(svg_data)

        bot.stats["games"] += 1
        if g % 10 == 0:
            output.eval_js(f"updateUI('{board.fen()}', 0, {len(bot.q_table)}, {bot.stats['wins']})")

    engine.quit()
    bot.save_brain()
    return "DOMINATION_COMPLETE"

output.register_callback('notebook.handle_move', handle_move)
output.register_callback('notebook.run_sims', run_sims)

