bot = WillyV8_Grandmaster()
board = chess.Board()

def get_status():
    if board.is_checkmate(): return "CHECKMATE!"
    if board.is_stalemate(): return "STALEMATE!"
    if board.is_game_over(): return "DRAW / GAME OVER"
    return "LIVE"

def handle_move(move_uci):
    global board
    if move_uci == "RESET":
        board.reset()
        return f"{board.fen()}|{bot.stats['games']}|{bot.stats['wins']}|READY"
    try:
        move = chess.Move.from_uci(move_uci)
        if move in board.legal_moves:
            board.push(move)
            if not board.is_game_over():
                board.push(bot.get_move(board))
    except: pass

    # If Willy played from book, let the user know
    history = " ".join([m.uci() for m in board.move_stack[:-1]])
    status = get_status()
    if status == "LIVE" and history in bot.book:
        status = "BOOK MOVE PLAYED"

    return f"{board.fen()}|{bot.stats['games']}|{bot.stats['wins']}|{status}"

def run_sims(count=10):
    global board
    count = int(count)
    is_spectator = True if count <= 10 else False
    try:
        engine = chess.engine.SimpleEngine.popen_uci("/usr/games/stockfish")
        engine.configure({"Skill Level": 5})
        for _ in range(count):
            board.reset()
            while not board.is_game_over() and board.fullmove_number < 100:
                move = engine.play(board, chess.engine.Limit(time=0.01)).move if board.turn == chess.WHITE else bot.get_move(board)
                board.push(move)
                if is_spectator:
                    output.eval_js(f"updateUI('{board.fen()}', {bot.stats['games']}, {bot.stats['wins']}, '{get_status()}')")
                    time.sleep(0.4)

            final_status = get_status()
            output.eval_js(f"updateUI('{board.fen()}', {bot.stats['games']}, {bot.stats['wins']}, '{final_status}')")
            bot.stats["games"] += 1
            if board.result() == "0-1": bot.stats["wins"] += 1
            bot.save_brain()
            if is_spectator: time.sleep(2.0)

        engine.quit()
    except Exception as e: print(f"Error: {e}")

output.register_callback('notebook.handle_move', handle_move)
output.register_callback('notebook.run_sims', run_sims)

