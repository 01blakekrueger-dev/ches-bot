    def save_brain(self):
        with open(DB_PATH, 'wb') as f:
            pickle.dump({'stats': self.stats}, f)

    def evaluate_board(self, board):
        if board.is_checkmate(): return 999999 if board.turn == chess.WHITE else -999999
        if board.is_stalemate() or board.is_insufficient_material(): return 0

        score = 0
        white_material = 0
        for sq, piece in board.piece_map().items():
            val = self.piece_values.get(piece.piece_type, 0)
            if piece.color == chess.WHITE: white_material += val

            if piece.color == chess.BLACK:
                score += val
                if piece.piece_type == chess.PAWN:
                    score += (7 - chess.square_rank(sq)) * 15
            else:
                score -= val

        # ENDGAME BOXING
        if white_material < 1500:
            w_king, b_king = board.king(chess.WHITE), board.king(chess.BLACK)
            score += max(abs(chess.square_rank(w_king) - 3.5), abs(chess.square_file(w_king) - 3.5)) * 15
            score += (14 - chess.distance(b_king, w_king)) * 5

        return score

    def check_opening_book(self, board):
        # Convert move history to a string to check against the dictionary
        history = " ".join([move.uci() for move in board.move_stack])
        if history in self.book:
            candidates = self.book[history]
            legal_moves = [chess.Move.from_uci(m) for m in candidates if chess.Move.from_uci(m) in board.legal_moves]
            if legal_moves:
                return random.choice(legal_moves)
        return None

    def minimax(self, board, depth, alpha, beta, maximizing):
        if depth == 0 or board.is_game_over(): return self.evaluate_board(board)
        moves = sorted(board.legal_moves, key=lambda m: board.is_capture(m), reverse=True)

        if maximizing:
            max_eval = -math.inf
            for move in moves:
                board.push(move)
                eval = self.minimax(board, depth - 1, alpha, beta, False)
                board.pop()
                max_eval = max(max_eval, eval)
                alpha = max(alpha, eval)
                if beta <= alpha: break
            return max_eval
        else:
            min_eval = math.inf
            for move in moves:
                board.push(move)
                eval = self.minimax(board, depth - 1, alpha, beta, True)
                board.pop()
                min_eval = min(min_eval, eval)
                beta = min(beta, eval)
                if beta <= alpha: break
            return min_eval

    def get_move(self, board):
        # 1. CHECK OPENING BOOK FIRST
        book_move = self.check_opening_book(board)
        if book_move:
            time.sleep(0.3) # Tiny delay to simulate playing
            return book_move

        # 2. FALLBACK TO MINIMAX SEARCH
        best_move = None
        max_val = -math.inf
        for move in board.legal_moves:
            board.push(move)
            val = self.minimax(board, 3, -math.inf, math.inf, False)
            board.pop()
            if val > max_val:
                max_val = val
                best_move = move
        return best_move if best_move else random.choice(list(board.legal_moves))

